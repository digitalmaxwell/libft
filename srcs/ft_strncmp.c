#include "../libft.h"

int ft_strncmp(const char *s1, const char *s2, size_t n)
{
    int i;

    i = 0;
    while (s1[i] && s2[i] && n--)
    {
        if (s1[i] != s2[i])
        {
            if (s1[i] > s2[i])
                return (1);
            else
                return (-1);
        }
    }
    return (0);
}